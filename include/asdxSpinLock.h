class SpinLock
{
    //=========================================================================
    // list of friend classes and methods.
    //=========================================================================
    /* NOTHING */

public:
    //=========================================================================
    // public variables.
    //=========================================================================
    /* NOTHING */

    //=========================================================================
    // public methods.
    //=========================================================================

    //-------------------------------------------------------------------------
    //! @brief      ロックします.
    //-------------------------------------------------------------------------
    void lock()
    {
        while (m_State.test_and_set(std::memory_order_acquire))
        { /* DO_NOTHING */ }
    }

    //-------------------------------------------------------------------------
    //! @brief      ロックを解除します.
    //-------------------------------------------------------------------------
    void unlock()
    { m_State.clear(std::memory_order_release); }

private:
    //=========================================================================
    // private variables.
    //=========================================================================
    std::atomic_flag    m_State = ATOMIC_FLAG_INIT;

    //=========================================================================
    // private methods.
    //=========================================================================
    /* NOTHING */
};

