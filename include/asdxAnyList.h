#pragma once


namespace asdx {

///////////////////////////////////////////////////////////////////////////////
// AnyList class
///////////////////////////////////////////////////////////////////////////////
class AnyList
{
    //=========================================================================
    // list of friend classes and methods.
    //=========================================================================
    /* NOTHING */

public:
    ///////////////////////////////////////////////////////////////////////////
    // Node class
    ///////////////////////////////////////////////////////////////////////////
    class Node
    {
        //=====================================================================
        // list of friend classes and methods.
        //=====================================================================
        friend class AnyList;

    public:
        //=====================================================================
        // public variables.
        //=====================================================================
        /* NOTHING */

        //=====================================================================
        // public methods.
        //=====================================================================

        //---------------------------------------------------------------------
        //! @brief      コンストラクタです.
        //---------------------------------------------------------------------
        explicit Node(void* value)
        : m_Data(value)
        , m_Next(nullptr)
        , m_Prev(nullptr)
        { /* DO_NOTHING */ }

        //---------------------------------------------------------------------
        //! @brief      デストラクタです.
        //---------------------------------------------------------------------
        ~Node()
        { Unlink(this); }

        //---------------------------------------------------------------------
        //! @brief      次のノードを取得します.
        //---------------------------------------------------------------------
        Node* GetNext() const
        { return m_Next; }

        //---------------------------------------------------------------------
        //! @brief      前のノードを取得します.
        //---------------------------------------------------------------------
        Node* GetPrev() const
        { return m_Prev; }

        //---------------------------------------------------------------------
        //! @brief      次のノードを持つかチェックします.
        //---------------------------------------------------------------------
        bool HasNext() const
        { return m_Next != nullptr; }

        //---------------------------------------------------------------------
        //! @brief      前のノードを持つかチェックします.
        //---------------------------------------------------------------------
        bool HasPrev() const
        { return m_Prev != nullptr; }

        //---------------------------------------------------------------------
        //! @brief      ポインタを取得します.
        //---------------------------------------------------------------------
        void* GetPtr() const
        { return m_Data; }

        //---------------------------------------------------------------------
        //! @brief      型を指定してポインタを取得します.
        //---------------------------------------------------------------------
        template<typename T>
        T* GetPtrAs() const
        { return reinterpret_cast<T*>(m_Data); }

    private:
        //=====================================================================
        // private variables.
        //=====================================================================
        Node*   m_Next = nullptr;
        Node*   m_Prev = nullptr;
        void*   m_Data = nullptr;

        //=====================================================================
        // private methods.
        //=====================================================================

        //---------------------------------------------------------------------
        //! @brief      リンクを設定します.
        //---------------------------------------------------------------------
        static void Link(Node* lhs, Node* rhs)
        {
            if (lhs == nullptr || rhs == nullptr)
            { return; }

            lhs->m_Next = rhs;
            rhs->m_Prev = lhs;
        }

        //---------------------------------------------------------------------
        //! @brief      リンクを解除します.
        //---------------------------------------------------------------------
        static void Unlink(Node* node)
        {
            if (node == nullptr)
            { return; }

            auto prev = node->m_Prev;
            auto next = node->m_Next;

            if (prev != nullptr)
            { prev->m_Next = next; }

            if (next != nullptr)
            { next->m_Prev = prev; }

            node->m_Prev = nullptr;
            node->m_Next = nullptr;
        }
    };

    //=========================================================================
    // public variables.
    //=========================================================================
    /* NOTHING */

    //=========================================================================
    // public methods.
    //=========================================================================

    //-------------------------------------------------------------------------
    //! @brief      コンストラクタです.
    //-------------------------------------------------------------------------
    AnyList()
    : m_Head (nullptr)
    , m_Tail (nullptr)
    , m_Count(0)
    { /* DO_NOTHING */ }

    //-------------------------------------------------------------------------
    //! @brief      デストラクタです.
    //-------------------------------------------------------------------------
    ~AnyList()
    { Clear(); }

    //-------------------------------------------------------------------------
    //! @brief      リストを破棄します.
    //-------------------------------------------------------------------------
    void Clear()
    {
        auto itr = m_Head;
        while(itr != nullptr)
        {
            auto node = itr;
            Node::Unlink(node);

            if (itr->m_Next == nullptr)
            { break; }

            itr = itr->m_Next;
        }

        m_Head = nullptr;
        m_Tail = nullptr;

        m_Count = 0;
    }

    //-------------------------------------------------------------------------
    //! @brief      先頭に追加します.
    //-------------------------------------------------------------------------
    void PushFront(Node* node)
    {
        if (node == nullptr)
        { return; }

        if (m_Head == nullptr)
        {
            m_Head = node;
            m_Tail = node;
        }
        else
        {
            Node::Link(node, m_Head);
            m_Head = node;
        }
        m_Count++;
    }

    //-------------------------------------------------------------------------
    //! @brief      末尾に追加します.
    //-------------------------------------------------------------------------
    void PushBack(Node* node)
    {
        if (node == nullptr)
        { return; }

        if (m_Head == nullptr)
        {
            m_Head = node;
            m_Tail = node;
        }
        else
        {
            Node::Link(m_Tail, node);
            m_Tail = node;
        }
        m_Count++;
    }

    //-------------------------------------------------------------------------
    //! @brief      先頭要素を取り出します.
    //-------------------------------------------------------------------------
    Node* PopFront()
    {
        if (m_Head == nullptr)
        { return nullptr; }

        auto head = m_Head;
        auto next = m_Head->Node::m_Next;
        Node::Unlink(head);
        m_Head = next;
        m_Count--;
        return head;
    }

    //-------------------------------------------------------------------------
    //! @brief      末尾要素を取り出します.
    //-------------------------------------------------------------------------
    Node* PopBack()
    {
        if (m_Tail == nullptr)
        { return nullptr; }

        auto tail = m_Tail;
        auto prev = m_Tail->Node::m_Prev;
        Node::Unlink(tail);
        m_Tail = prev;
        m_Count--;
        return tail;
    }

    //-------------------------------------------------------------------------
    //! @brief      ノードを挿入します.
    //-------------------------------------------------------------------------
    void Insert(Node* target, Node* node)
    {
        if (target == nullptr || node == nullptr)
        { return; }

        Node::Link(target, node);
        if (target == m_Tail)
        { m_Tail = node; }

        m_Count++;
    }

    //-------------------------------------------------------------------------
    //! @brief      指定ノードをリストから削除します.
    //-------------------------------------------------------------------------
    void Remove(Node* node)
    {
        if (node == nullptr)
        { return; }

        if (node == m_Tail)
        { m_Tail = node->m_Prev; }
        Node::Unlink(node);
        m_Count--;
    }

    //-------------------------------------------------------------------------
    //! @brief      指定ノードが含まれるかチェックします.
    //-------------------------------------------------------------------------
    bool Contains(const Node* node) const
    {
        if (node == nullptr)
        { return false; }

        auto itr = m_Head;
        while(itr != nullptr)
        {
            if (itr == node)
            { return true; }

            if (itr->m_Next == nullptr)
            { break; }

            itr = itr->m_Next;
        }

        return false;
    }

    //-------------------------------------------------------------------------
    //! @brief      空かどうかチェックします.
    //-------------------------------------------------------------------------
    bool IsEmpty() const
    { return m_Count == 0; }

    //-------------------------------------------------------------------------
    //! @brief      先頭ノードを取得します.
    //-------------------------------------------------------------------------
    Node* GetHead() const
    { return m_Head; }

    //-------------------------------------------------------------------------
    //! @brief      末尾ノードを取得します.
    //-------------------------------------------------------------------------
    Node* GetTail() const
    { return m_Tail; }

    //-------------------------------------------------------------------------
    //! @brief      ノード数を取得します.
    //-------------------------------------------------------------------------
    size_t GetCount() const
    { return m_Count; }

private:
    //=========================================================================
    // private variables.
    //=========================================================================
    Node*   m_Head  = nullptr;
    Node*   m_Tail  = nullptr;
    size_t  m_Count = 0;

    //=========================================================================
    // private methods.
    //=========================================================================
    /* NOTHING */
};

} // namespace asdx
