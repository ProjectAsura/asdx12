#pragma once

//-----------------------------------------------------------------------------
// Includes
//-----------------------------------------------------------------------------
#include <asdxGraphicsDevice.h>


namespace asdx {

///////////////////////////////////////////////////////////////////////////////
// SAMPLER_TYPE enum
///////////////////////////////////////////////////////////////////////////////
enum SAMPLER_TYPE
{
    ST_POINT_CLAMP,
    ST_POINT_WRAP,
    ST_POINT_MIRROR,
    ST_LINEAR_CLAMP,
    ST_LINEAR_WRAP,
    ST_LINEAR_MIRROR,
    ST_ANISOTROPIC_CLAMP,
    ST_ANISOTROPIC_WRAP,
    ST_ANISOTROPIC_MIRROR,
};

///////////////////////////////////////////////////////////////////////////////
// Sampler class
///////////////////////////////////////////////////////////////////////////////
class Sampler
{
    //=========================================================================
    // list of friend classes and methods.
    //=========================================================================
    /* NOTHING */

public:
    //=========================================================================
    // public variables.
    //=========================================================================
    /* NOTHING */

    //=========================================================================
    // public methods.
    //=========================================================================

    //-------------------------------------------------------------------------
    //! @brief      コンストラクタです.
    //-------------------------------------------------------------------------
    Sampler();

    //-------------------------------------------------------------------------
    //! @brief      デストラクタです.
    //-------------------------------------------------------------------------
    ~Sampler();

    //-------------------------------------------------------------------------
    //! @brief      初期化処理を行います.
    //!
    //! @param[in]      device          グラフィックスデバイスです.
    //! @param[in]      type            サンプラータイプです.
    //! @param[in]      compareFunc     比較関数.
    //! @retval true    初期化に成功.
    //! @retval false   初期化に失敗.
    //-------------------------------------------------------------------------
    bool Init(
        GraphicsDevice&         device,
        SAMPLER_TYPE            type,
        D3D12_COMPARISON_FUNC   compareFunc = D3D12_COMPARISON_FUNC_ALWAYS);

    //-------------------------------------------------------------------------
    //! @brief      初期化処理を行います.
    //!
    //! @param[in]      device      グラフィックスデバイスです.
    //! @param[in]      pDesc       構成設定です.
    //! @retval true    初期化に成功.
    //! @retval false   初期化に失敗.
    //-------------------------------------------------------------------------
    bool Init(GraphicsDevice& device, const D3D12_SAMPLER_DESC* pDesc);

    //-------------------------------------------------------------------------
    //! @brief      終了処理を行います.
    //-------------------------------------------------------------------------
    void Term();

    //-------------------------------------------------------------------------
    //! @brief      ディスクリプターを取得します.
    //!
    //! @return     ディスクリプターを返却します.
    //-------------------------------------------------------------------------
    const Descriptor* GetDescriptor() const;

private:
    //=========================================================================
    // private variables.
    //=========================================================================
    asdx::RefPtr<Descriptor>    m_Descriptor;

    //=========================================================================
    // private methods.
    //=========================================================================
    /* NOTHING */
};

} // namespace asdx
