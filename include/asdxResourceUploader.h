#pragma once

//-----------------------------------------------------------------------------
// Includes
//-----------------------------------------------------------------------------
#include <queue>
#include <list>
#include <mutex>
#include <asdxUpdateResource.h>


namespace asdx {

///////////////////////////////////////////////////////////////////////////////
// ResourceUploader class
///////////////////////////////////////////////////////////////////////////////
class ResourceUploader
{
    //=========================================================================
    // list of friend classes and methods.
    //=========================================================================
    /* NOTHING */

public:
    //=========================================================================
    // public variables.
    //=========================================================================

    //-------------------------------------------------------------------------
    //! @brief      コンストラクタです.
    //-------------------------------------------------------------------------
    ResourceUploader();

    //-------------------------------------------------------------------------
    //! @brief      デストラクタです.
    //-------------------------------------------------------------------------
    ~ResourceUploader();

    //-------------------------------------------------------------------------
    //! @brief      リソースを登録します.
    //!
    //! @param[in]      pItem       登録するリソース.
    //! @note       内部で参照カウントは上げません.
    //-------------------------------------------------------------------------
    void Push(IUploadResource* pItem);

    //-------------------------------------------------------------------------
    //! @brief      アップロード処理を実行します.
    //!
    //! @param[in]      pCmdList        コマンドリストです.
    //-------------------------------------------------------------------------
    void Upload(ID3D12GraphicsCommandList* pCmdList);

    //-------------------------------------------------------------------------
    //! @brief      フレーム同期し，遅延解放を実行します.
    //-------------------------------------------------------------------------
    void FrameSync();

    //-------------------------------------------------------------------------
    //! @brief      強制破棄を実行します.
    //!
    //! @note       内部でIUploadResource::Resource()を呼び出します.
    //-------------------------------------------------------------------------
    void Clear();

private:
    ///////////////////////////////////////////////////////////////////////////
    // Item structure
    ///////////////////////////////////////////////////////////////////////////
    struct Item
    {
        IUploadResource*    pResource;      //!< 破棄リソース.
        uint32_t            LifeTime;       //!< 生存フレーム数.
    };

    //=========================================================================
    // private varaibles.
    //=========================================================================
    std::queue<IUploadResource*>    m_Queue;        //!< リソースキュー.
    std::list<Item>                 m_List;         //!< 破棄リスト.

    //=========================================================================
    // private methods.
    //=========================================================================
    /* NOTHING */
};

} // namespace asdx
