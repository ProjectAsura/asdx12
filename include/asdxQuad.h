#pragma once

//-----------------------------------------------------------------------------
// Includes
//-----------------------------------------------------------------------------
#include <asdxVertexBuffer.h>


namespace asdx {

///////////////////////////////////////////////////////////////////////////////
// Quad class
///////////////////////////////////////////////////////////////////////////////
class Quad
{
    //=========================================================================
    // list of friend classes and methods.
    //=========================================================================
    /* NOTHING */

public:
    //=========================================================================
    // public variables.
    //=========================================================================
    static const D3D12_INPUT_LAYOUT_DESC InputLayout;

    //=========================================================================
    // public methods.
    //=========================================================================

    //-------------------------------------------------------------------------
    //! @brief      シングルトンインスタンスを取得します.
    //!
    //! @return     シングルトンインスタンスを返却します.
    //-------------------------------------------------------------------------
    static Quad& Instance();

    //-------------------------------------------------------------------------
    //! @brief      初期化済みかどうか確認します.
    //!
    //! @retval true    初期化済みです.
    //! @retval false   未初期化です.
    //-------------------------------------------------------------------------
    bool IsInit() const;

    //-------------------------------------------------------------------------
    //! @brief      初期化処理を行います.
    //!
    //! @param[in]      device      グラフィックスデバイスです.
    //! @retval true    初期化に成功.
    //! @retval false   初期化に失敗.
    //-------------------------------------------------------------------------
    bool Init(GraphicsDevice& device);

    //-------------------------------------------------------------------------
    //! @brief      終了処理を行います.
    //-------------------------------------------------------------------------
    void Term();

    //-------------------------------------------------------------------------
    //! @brief      描画処理を行います.
    //!
    //! @param[in]      pCmd        コマンドリストです,
    //-------------------------------------------------------------------------
    void Draw(ID3D12GraphicsCommandList* pCmd);

private:
    //=========================================================================
    // private variables.
    //=========================================================================
    static Quad     s_Instance; //!< シングルトンインスタンスです.
    bool            m_Init;     //!< 初期化済みフラグ.
    VertexBuffer    m_VB;       //!< 頂点バッファ.

    //=========================================================================
    // private methods.
    //=========================================================================
    Quad();
    ~Quad();

    Quad& operator = (const Quad&) = delete;
};


} // namespace asdx
